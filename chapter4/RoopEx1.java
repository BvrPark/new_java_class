package chapter4;

public class RoopEx1 {

	public static void main(String[] args) {
		
		int i;
		int sum = 0;
		
		for(i = 0; i <= 100; i++) {                                 //for(초기조건;조건;처리부분을,로 두개넣을수 있다(ex)i++,i++)
			sum += i;
			System.out.println("sum : " + sum + ", " + "i : " + i);
		}
		
		System.out.println(i);

	}

}
